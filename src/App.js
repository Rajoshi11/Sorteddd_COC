import React, { useState, useEffect } from 'react';
import Spline from "@splinetool/react-spline";
import styled from "styled-components";
import TrackVisibility from 'react-on-screen';
import ReactCompareImage from 'react-compare-image';
import img1 from './assets/before.png'
import img2 from './assets/after.png'

export default function App() {
  const [loopNum, setLoopNum] = useState(0);
  const [isDeleting, setIsDeleting] = useState(false);
  const [text, setText] = useState('');
  const [index, setIndex] = useState(1);
  const [delta, setDelta] = useState(300 - Math.random() * 100);
  const toRotate = [ "Let's catch the match!" ];
  const period = 1500;

  useEffect(() => {
    let ticker = setInterval(() => {
      tick();
    }, delta);

    return () => { clearInterval(ticker) };
  }, [text])

  const tick = () => {
    let i = loopNum % toRotate.length;
    let fullText = toRotate[i];
    let updatedText = isDeleting ? fullText.substring(0, text.length - 1) : fullText.substring(0, text.length + 1);

    setText(updatedText);

    if (isDeleting) {
      setDelta(prevDelta => prevDelta / 2);
    }

    if (!isDeleting && updatedText === fullText) {
      setIsDeleting(true);
      setIndex(prevIndex => prevIndex - 1);
      setDelta(period);
    } else if (isDeleting && updatedText === '') {
      setIsDeleting(false);
      setLoopNum(loopNum + 1);
      setIndex(1);
      setDelta(500);
    } else {
      setIndex(prevIndex => prevIndex + 1);
    }
  }
  return (
    <Wrapper>
      <div
        style={{
          backgroundSize: 'cover',
          display: 'flex',
          justifyContent: 'center',
          alignItems: 'center',
          backgroundColor: '#fba4c4',
          height: '100vh'
        }}
      ><Spline scene="https://prod.spline.design/0au9Qy8Tn2a5MENt/scene.splinecode" style={{height:'100vh', position: 'absolute', zIndex:1 }}/></div>
      <Social>
        <div />
      </Social>
      <Content>
        <Menu>
          <li>
            {/* <img src={Logo} alt="title" /> */}
          </li>
          <li>
            <a href="/">Home</a>
          </li>
          <li>
            <a href="/">Download</a>
          </li>
          <li>
            <a href="/">App</a>
          </li>
          <li>
            <a href="/">Login</a>
          </li>
          <li>
            <button>Get Started</button>
          </li>
        </Menu>

        <TrackVisibility>
            {({ isVisible }) =>
            <div className={isVisible ? "animate__animated animate__fadeIn" : ""}>
              <h1>{``} <span className="txt-rotate" dataperiod="1000" data-rotate='[ "Lets catch the match!" ]'><span className="wrap tracking-tight">{text}</span></span></h1>
                <br></br>
                <br></br>
                <br></br>
            </div>}
          </TrackVisibility>
          <p style={{fontSize: '30px'}}>It is never too late to fall in love!</p>
        <button>
           Download now!
        </button>
      </Content>
      <div
        style={{
          display: 'flex',
          justifyContent: 'center',
          alignItems: 'center',
          backgroundColor: '#fba4c4',
          height: '50vh',
          overflow:'hidden',
          width: '100vh',
          padding: '10px'
        }}
      >
    {/* <div >      */}
    <ReactCompareImage style={{ width:'50px', height:'50px'}} leftImage={img1} rightImage={img2} withResizeFeel= "false" />
    </div>  
    {/* </div> */}
    </Wrapper>
  );
}

